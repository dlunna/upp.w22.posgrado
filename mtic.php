                <br />
               <p class="titulo">Objetivos de las líneas de investigación</p>
               <p align="justify">1. Diseñar, modelar e implementar sistemas de información mediante la aplicación de modelos de inteligencia y seguridad computacional, así como el diseño de algoritmos para el procesamiento de imágenes, que resuelvan las necesidades de la sociedad y la aplicación de la investigación en la solución de problemas.
               <br>
               2. Investigar, analizar e implementar sistemas embebidos, sistemas de comunicación remota, involucrando diferentes técnicas de diseño de software y hardware en ciencia y tecnología básica y aplicada.
               </p>
               <p class="titulo">Descripción líneas de investigación LGAC</p>
               <p class="titulo">Desarrollo de Sistemas de Información </p>
               <p align="justify">En esta LGAC se realiza diseño, modelado de sistemas inteligentes, protocolos de seguridad computacional, criptografía, así como algoritmos para el procesamiento de imágenes y reconocimiento de patrones, con el uso adecuado de la información. Un criterio que asegura la congruencia de esta LGAC con los objetivos del programa, está relacionado con el manejo de la información de sistemas de seguridad informática y criptografía, sistemas dinámicos, desarrollo de algoritmos y agricultura de precisión. Otro criterio importante que se considera para asegurar la congruencia de esta LGAC con los objetivos del programa, es que el 50% de las asignaturas da el soporte académico y científico a los estudiantes que realizan sus estudios en el programa educativo.</p>
               <p class="titulo">Desarrollo con Sistemas Embebidos y Telecomunicaciones</p>
               <p align="justify">Esta LGAC da soporte a los estudiantes del programa educativo que realizan sus temas de tesis orientado al estudio y desarrollo de sistemas embebidos y telecomunicaciones, en esta dirección se han desarrollado recientemente temas de tesis en donde los estudiantes diseñan y desarrollan sistemas embebidos con algoritmo inteligentes embarcados, por ejemplo sistema energía con paneles solares para invernaderos, modelado y control inteligente para los ciclos de riego en invernaderos, sistemas para la medición y predicción de problemas de salud, monitoreo y control de plagas en invernaderos agrícolas. EL 50% de las asignaturas optativas del programa educativo que actualmente se ofrece da soporte académico y científico a los estudiantes que realizan temas de tesis que inciden en esta LGAC. Hay asignaturas optativas que inciden entre ambas LGAC, como, por ejemplo: Inteligencia artificial, procesamiento de imágenes y procesamiento de señales, estas asignaturas se imparten con el enfoque del estudio de: la algoritmia, la implementación en sistemas embebidos y el manejo de la información, que son bases de datos de plataformas experimentales, imágenes y señales.</p>
