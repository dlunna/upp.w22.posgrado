                  <p class="titulo">Vinculación </p>
                  <p align="justify">Desde su creación el PE-MTIC ha realizado un programa permanente de vinculación, inicialmente se disponía solo de la dirección de Vinculación, que se encargaba de establecer los convenios con los diferentes sectores de la sociedad principalmente a nivel nacional, sin embargo debido a la naturaleza del Programa y del crecimiento de la Universidad, fue necesario la especialización de esta área, puesto que los estudiantes y profesores empezaron a extender sus investigaciones y relaciones a nivel internacional, es así que se conformaron dos nuevos departamentos en la UP-Pachuca a) El departamento de Internacionalización (conformado en abril de 2014), que brinda apoyo para el trámite de movilidades, firmas de convenios en el ámbito internacional y b) El departamento de Programas Especiales que brinda la atención al sector productivo mediante el seguimiento administrativo de los proyectos de investigación, firmas de convenios para proyectos especiales derivados de la baja de recursos de profesores o Proyectos con financiamiento PRODEP y CONACYT como el Programa de Estímulos a la Innovación. Potenciando así los ámbitos de la Universidad y por ende de este posgrado. Para ello se incorporó a Vinculación al Sistema de Gestión de la Calidad (SGC) mediante los procesos: a) P-VI-03 Seguimiento de Egresados que permite realizar el seguimiento de egresados de posgrado y b) P-VI-02 Convenios. Que establece los lineamientos para la firma de convenios. El resultado de vinculación a través de los diferentes sectores, ha permitido tener un total de 18 convenios vigentes de los cuales 10 son internacionales, 7 con Universidades y centros de investigación nacional y uno con empresas.</p>
                <center><a href="./images/ca_mtic/clasificacion_de_los_convenios_2018.pdf" target="_blank"><img src="./images/ICONOPDF.png" width="100" height="100" alt="IconoPDF" /></a></center>
                <p align="justify">Además de la participación en Proyectos PEI, se tiene acceso a fondos concursables como el PRODEP, en el cual en 2016 se obtuvo recursos por parte de los investigadores y el Programa PROFOCIE, además de Proyectos como el Programa de Fortalecimiento de la Calidad Educativa (PFCE) que es una iniciativa de la SEP para el otorgamiento de recursos financieros extraordinarios, para que a partir de ejercicios de planeación estratégica participativa implementen las Instituciones de Educación Superior (IES) proyectos académicos para la mejora de la gestión (servicios) y que impacten en la calidad de sus programas educativos, a fin de que contribuyan al desarrollo de México.</p>
