<b><p class="titulo">Perfil de ingreso</p></b>
<p align="justify">
Podrán aspirar a ingresar a la Maestría en Finanzas y Gestión egresados de licenciaturas en áreas económico-administrativas: Administración, Economía, Contaduría Pública, Ingeniería Financiera, Comercio, Logística, Derecho e ingenierías afines. El aspirante a ingresar al PE-MGF deberá reunir ciertas características que pudiesen darle una buena plataforma para el curso de su posgrado:
Conocimientos indispensables en:</p>

<ul style="text-align:justify">
   <li>Conocimientos básicos de fundamentos de administración, economía y contabilidad, con el objeto de involucrarlos de manera inmediata a los procesos de enseñanza- aprendizaje, de cada curso que conforma el Programa Educativo. </li>
   <li>Comprensión de textos técnicos y científicos en el idioma de inglés.</li>
   <li>Métodos estadísticos.</li>
   <li>Habilidad Lógica y capacidad de análisis en la comprensión. Teniendo una base Matemática, de tipo formativo para el desarrollo de habilidades de abstracción y la expresión de formalismos, además de que le ayudan a desarrollar conocimientos específicos fundamentales para la aplicación de las finanzas.</li>
 </ul> 
<p align="justify">Habilidades necesarias:</p>

<ul style="text-align:justify">
   <li>Responsabilidad, capacidad de trabajo individual y en equipo.</li>
   <li>Habilidad de análisis y síntesis.</li>
   <li>Espíritu crítico y auto crítico.</li>
   <li>Habilidad para comunicarse efectivamente de manera oral y escrita.</li>
   <li>Capacidad para relacionarse e ingresarse en equipos de trabajo. Donde comprenda conocimientos, normas, experiencias y motivaciones que necesitara para hacer posible la buena integración de las unidades financieras y su personal en las organizaciones y en la sociedad en general.</li>
   <li>Afinidad e interés por las áreas de las finanzas y la gestión.</li>
   <li>Mostrar inclinación a la creatividad e innovación, capacidad de entendimiento de las ciencias, predisposición para el autoaprendizaje; así como contar con un amplio sentido de responsabilidad, orden y disciplina.</li>
