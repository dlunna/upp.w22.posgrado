<p class="titulo">Perfil de egreso</p>
<p align="justify">
La formación del Maestro en Finanzas y Gestión en la UPPachuca, es integral y multidisciplinaria, y se realiza mediante un programa académico intensivo basado en competencias, que le permite adquirir conocimientos, actitudes, habilidades y destrezas para automatizar procesos y desarrollar o actualizar sistemas a través de la integración de dos áreas o disciplinas que son las finanzas y la gestión; lo que permite al egresado ser más competitivo, poder interactuar en diferentes campos de acción y tener un mejor desempeño en la empresa, institución u organización pública o privada de los sectores industrial, comercial y de servicios.</p>
<p align="justify">
El egresado de la Maestría en Finanzas y Gestión es un profesional capaz de desempeñarse en el campo de las Finanzas y Gestión de negocios, Diagnosticando procesos empresariales, formulando planes estratégicos y proyectos de inversión, diseñando estrategias de inversión y financiamiento, determinando propuestas de modelos de gestión financiera, administrar estrategias, medir procesos y acciones empresariales en la dirección estratégica y financiera de las entidades.
</p>
